/** Look up a county preset by name (case-insensitive). */
export function getCountyPreset(countyName: string): KenyaCountyIrradiance | undefined {
  const q = countyName.trim().toLowerCase();
  return KENYA_COUNTY_IRRADIANCE.find((c) => c.county.toLowerCase() === q);
}

/** Return all KOSAP priority counties. */
export function getKosapCounties(): KenyaCountyIrradiance[] {
  return KENYA_COUNTY_IRRADIANCE.filter((c) => c.isKosap);
}

/**
 * Get the PSH for a county and month.
 * @param county County name (case-insensitive)
 * @param monthIndex 0-based month index (0=Jan, 11=Dec)
 * @returns PSH in kWh/m²/day, or the county annual average if not found
 */
export function getMonthlyPsh(
  county: string,
  monthIndex: number
): number {
  const preset = getCountyPreset(county);
  if (!preset) return 5.5; // Nairobi fallback
  return preset.monthlyPsh[Math.max(0, Math.min(11, monthIndex))];
}

/**
 * Get the ambient temperature for a county and month.
 * @param county County name (case-insensitive)
 * @param monthIndex 0-based month index (0=Jan, 11=Dec)
 * @returns Temperature in °C, or 25 if not found
 */
export function getMonthlyTemp(
  county: string,
  monthIndex: number
): number {
  const preset = getCountyPreset(county);
  if (!preset) return 25;
  return preset.monthlyTempC[Math.max(0, Math.min(11, monthIndex))];
}
